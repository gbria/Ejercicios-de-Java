package Objetivo4;

public class O4_Ej3 {
    public static void main(String[] args) {
        String cadena = "en un lugar de la mancha";
        System.out.println("Original: " + cadena);
        System.out.println("Length: " + cadena.length());
        cadena = cadena.toUpperCase();
        System.out.println("ToUpper: " + cadena);
        System.out.println("ToLower: " + cadena.toLowerCase());
    }
}
