package Objetivo3;

public class O3_Ej8 {
    public static void main(String[] args) {
        boolean valueTrue = true;
        boolean valueFalse = false;

        System.out.println("True AND True = " + (valueTrue && valueTrue));
        System.out.println("True AND False = " + (valueTrue && valueFalse));
        System.out.println("False AND False = " + (valueFalse && valueFalse));
    }
}
